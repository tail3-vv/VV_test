void led_g_toggle(void) {
    nrf_gpio_pin_toggle(LEDG_PIN_NUMBER);
}

void led_g_on(void) {
    nrf_gpio_pin_set(LEDG_PIN_NUMBER);
}

void led_g_off(void) {
    nrf_gpio_pin_clear(LEDG_PIN_NUMBER);
}

void led_g_blink(int numOfBlinks)
{
    //blink both leds and then turn them off
    while (numOfBlinks > 0)
    {
        nrf_gpio_pin_set(LEDG_PIN_NUMBER);
        nrf_delay_ms(250);
        nrf_gpio_pin_clear(LEDG_PIN_NUMBER);
        nrf_delay_ms(250);
        numOfBlinks--;
    }
    nrf_gpio_pin_clear(LEDG_PIN_NUMBER);
}

